/**
 * Current language code (default: 'ru')
 * @type {string}
 */
let currentLang = localStorage.getItem('lang') || 'ru';

/**
 * Translate a key to the current language
 * @param {string} key - Translation key from the translations object
 * @returns {string} Translated string or the key itself if not found
 */
function translate(key) {
  return translations[currentLang][key] || key;
}

/**
 * Get full URL for the country flag SVG (circle-flags CDN)
 * @param {string} lang - Language code
 * @returns {string} Full URL to the flag image
 */
function getFlagUrl(lang) {
  return `https://circle-flags.cdn.skk.moe/flags/${translations[lang].flag}.svg`;
}

/**
 * Switch the current language and refresh all UI texts
 * @param {string} lang - Language code to set
 */
function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  renderAllTexts();
  updateLanguageButton();
  toggleDropdown();
  if (typeof renderTodayHeader === 'function') renderTodayHeader();
  if (typeof renderCalendar === 'function' && Object.keys(historyData || {}).length >= 0) {
    renderCalendar();
  }
  if (typeof renderFoods === 'function') renderFoods();
  if (typeof renderTodayLogs === 'function') renderTodayLogs();
  // persist to backend so bot stays in sync
  if (typeof tgId !== 'undefined' && tgId !== 123456789) {
    apiFetch('/api/profile/language', 'POST', { tg_id: tgId, language: lang });
  }
}

/**
 * Update the current language code button in the header
 */
function updateLanguageButton() {
  const iconWrap = document.getElementById('lang-icon-wrap');
  const label = document.getElementById('lang-toggle-label');
  if (iconWrap) {
    iconWrap.innerHTML = `<img src="${getFlagUrl(currentLang)}" alt="${currentLang}" style="width:18px;height:18px;border-radius:50%;object-fit:cover;">`;
  }
  if (label) label.textContent = translations[currentLang]?.name || currentLang.toUpperCase();
}

/**
 * Toggle the language dropdown visibility
 */
function toggleDropdown() {
  const dropdown = document.getElementById('lang-dropdown');
  const isOpen = dropdown.classList.toggle('open');
  if (isOpen) renderLanguageDropdown();
}

/**
 * Render all available languages inside the dropdown
 */
function renderLanguageDropdown() {
  const dropdown = document.getElementById('lang-dropdown');
  dropdown.innerHTML = '';
  Object.keys(translations).forEach(lang => {
    const div = document.createElement('div');
    div.className = 'lang-option';
    div.innerHTML = `
      <img src="${getFlagUrl(lang)}" class="lang-option-flag" alt="${translations[lang].name}">
      <span>${translations[lang].name}</span>`;
    div.onclick = () => setLanguage(lang);
    dropdown.appendChild(div);
  });
}

/**
 * Update all translatable texts and placeholders on the page
 * Called automatically on language change and unit change
 */
function renderAllTexts() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[currentLang][key]) el.textContent = translations[currentLang][key];
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[currentLang][key]) el.placeholder = translations[currentLang][key];
  });
}

/**
 * Initialize i18n on page load
 * Forces Russian as default if no language was saved
 */
window.onload = () => {
  updateLanguageButton();
  renderAllTexts();
  renderLanguageDropdown();
};